// ============ SMOOTH SCROLL ============
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
  anchor.addEventListener('click', function (e) {
    e.preventDefault();
    const target = document.querySelector(this.getAttribute('href'));
    if (target) {
      target.scrollIntoView({ behavior: 'smooth' });
    }
  });
});

// ============ SCROLL REVEAL ============
function initReveal() {
  const elements = document.querySelectorAll(
    '.service-card, .usecase-card, .resource-card, section > div > .text-center, section > div > .grid'
  );
  elements.forEach(el => el.classList.add('reveal'));

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
  });

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
}

// ============ CONTACT FORM ============
document.getElementById('contact-form').addEventListener('submit', function(e) {
  e.preventDefault();

  const formData = new FormData(this);
  const name = formData.get('name');
  const email = formData.get('email');
  const company = formData.get('company');
  const subject = formData.get('subject');
  const message = formData.get('message');

  const mailBody = `Nom: ${name}%0D%0AEmail: ${email}%0D%0AEntreprise: ${company}%0D%0ASujet: ${subject}%0D%0A%0D%0A${message}`;
  const mailSubject = encodeURIComponent(`[EMPIRA] ${subject} - ${name}`);

  window.location.href = `mailto:info@empira.be?subject=${mailSubject}&body=${mailBody}`;
});

