// ============ LANGUAGE SWITCHER ============
let currentLang = localStorage.getItem('empira-lang') || 'fr';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('empira-lang', lang);
  document.documentElement.lang = lang;

  // Update all translatable elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Update meta tags
  if (lang === 'en') {
    document.title = 'EMPIRA — AI Consultant in Brussels | Training & Artificial Intelligence Support';
    document.querySelector('meta[name="description"]').content = 'EMPIRA, your AI specialist in Brussels and Wallonia. AI training, personalised support, audit and AI strategy for businesses and SMEs.';
  } else {
    document.title = 'EMPIRA — Consultant IA à Bruxelles | Formation & Accompagnement Intelligence Artificielle';
    document.querySelector('meta[name="description"]').content = 'EMPIRA, votre spécialiste en intelligence artificielle à Bruxelles et Wallonie. Formations IA, accompagnement personnalisé, audit et stratégie IA pour entreprises et PME.';
  }

  // Update active button state
  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
  document.getElementById('lang-' + lang)?.classList.add('active');
  document.getElementById('lang-' + lang + '-m')?.classList.add('active');
}

// ============ NAVBAR SCROLL ============
const navbar = document.getElementById('navbar');

